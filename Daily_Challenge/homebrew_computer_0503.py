#!/usr/bin/env python3

notes = {
    261: "C4",
    294: "D4",
    329: "E4",
    349: "F4",
    392: "G4",
    440: "A4",
    494: "B4",
    523: "C5",
    0:   "REST",
}


def altair_to_notes(switches):
    return [notes[int(s, 2)] for s in switches]


# Examples
print(altair_to_notes(["0100000101", "0100000101", "0110001000", "0110001000", "0110111000", "0110111000", "0110001000", "0000000000"]))
print(altair_to_notes(["0101001001", "0101001001", "0101001001", "0000000000", "0101001001", "0101001001", "0101001001", "0000000000", "0101001001", "0110001000", "0100000101", "0100100110", "0101001001", "0000000000", "0000000000"]))
print(altair_to_notes(["0100000101", "0000000000", "0100000101", "0100100110", "0000000000", "0100000101", "0000000000", "0101011101", "0000000000", "0101001001", "0000000000", "0000000000"]))
